# This script is to perform arithmetic operations with long variables.

this_is_the_first_1_variable_to_execute__operations = 155.5
this_is_the_second_2_variable_to_execute_operations = 11.5

# Execute sum
print("The sum result is:", this_is_the_first_1_variable_to_execute__operations +
      this_is_the_second_2_variable_to_execute_operations)

# Execute a rest
print("The rest result is:", this_is_the_first_1_variable_to_execute__operations -
      this_is_the_second_2_variable_to_execute_operations)

# Execute a multiply
print("The multiply result is:", this_is_the_first_1_variable_to_execute__operations *
      this_is_the_second_2_variable_to_execute_operations)

# Execute a division
print("The division result is:", this_is_the_first_1_variable_to_execute__operations /
      this_is_the_second_2_variable_to_execute_operations)

# Execute a modulus
print("The remainder result is:", this_is_the_first_1_variable_to_execute__operations %
      this_is_the_second_2_variable_to_execute_operations)

# Execute a exponential
print("The exponential result is:", this_is_the_first_1_variable_to_execute__operations **
      this_is_the_second_2_variable_to_execute_operations)

# Execute a floor division
print("The floor division result is:", this_is_the_first_1_variable_to_execute__operations //
      this_is_the_second_2_variable_to_execute_operations)
